        cat.getBoundingClientRect().top >= 0
      );
      
      let nextIndex;
      if (swipeDistance > 0) {
        // Swipe down - go to previous
        nextIndex = currentIndex <= 0 ? categories.length - 1 : currentIndex - 1;
      } else {
        // Swipe up - go to next
        nextIndex = (currentIndex + 1) % categories.length;
      }
      
      categories[nextIndex].scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
  }
  
  // Initialize water flow animation for desktop only
  if (window.innerWidth > 768) {
    const waterWordsList = document.querySelector('.water-words-list');
    if (waterWordsList) {
      waterWordsList.style.setProperty('--water-flow', 'running');
    }
  }
});
