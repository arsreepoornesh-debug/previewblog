  });
  
  // Add subtle parallax effect to floating elements
  window.addEventListener('scroll', function() {
    const scrolled = window.pageYOffset;
    const rate = scrolled * -0.5;
    
    // Tamil floating elements
    const floatingElements = document.querySelectorAll('.floating-element, .floating-wisdom');
    floatingElements.forEach((el, index) => {
      const speed = 0.3 + (index * 0.1);
      el.style.transform = `translateY(${rate * speed}px)`;
    });
  });
  
  // Add click effect on wisdom quotes
  const quotes = document.querySelectorAll('.wisdom-quote');
  quotes.forEach(quote => {
    quote.addEventListener('click', function() {
      // Create a temporary highlight effect
      const originalBorder = this.style.borderLeft;
      this.style.borderLeft = '4px solid var(--tamil-red)';
      this.style.transform = 'scale(1.02)';
      
      setTimeout(() => {
        this.style.borderLeft = originalBorder;
        this.style.transform = '';
      }, 500);
      
      // Copy quote text to clipboard
      const quoteText = this.querySelector('p').innerText;
      navigator.clipboard.writeText(quoteText).then(() => {
