  const mathNumbersRegex = /(\d{1,3}(?:,\d{3})*(?:\.\d+)?)/g;
  
  // Process fact boxes
  const factBoxes = document.querySelectorAll('.fact-box p');
  factBoxes.forEach(p => {
    let html = p.innerHTML;
    
    // Highlight English words
    html = html.replace(englishWordsRegex, '<span class="english-word">$1</span>');
    
    // Highlight numbers (but not dates like 1939, 1876, etc.)
    html = html.replace(mathNumbersRegex, function(match) {
      // Don't highlight 4-digit numbers (likely years)
      if (/^\d{4}$/.test(match)) return match;
      return `<span class="math-number">${match}</span>`;
    });
    
    p.innerHTML = html;
  });
  
